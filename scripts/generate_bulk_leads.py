def collect_existing_titles():
    """Collect titles from existing problems and leads for dedup."""
    titles = set()
    for f in PROBLEMS_DIR.rglob("*.yaml"):
        try:
            with open(f) as fh:
                d = yaml.safe_load(fh)
                if d and "title" in d:
                    titles.add(d["title"].strip().lower())
        except Exception:
            pass
    for f in LEADS_DIR.glob("*.yaml"):
        try:
            with open(f) as fh:
                d = yaml.safe_load(fh)
                if d and "title" in d:
                    titles.add(d["title"].strip().lower())
        except Exception:
            pass
    return titles

def make_slug(title):
    slug = title.lower()
    slug = re.sub(r"[^a-z0-9-]", "-", slug)
    slug = re.sub(r"-{2,}", "-", slug)
    slug = slug.strip("-")
    return slug[:60].rstrip("-")

def main():
    existing = collect_existing_titles()
    print(f"Existing titles (for dedup): {len(existing)}")
    LEADS_DIR.mkdir(parents=True, exist_ok=True)

    generated = 0
    skipped = 0
    for title, statement, domain, subdomain, url in CURATED_PROBLEMS:
        if title.strip().lower() in existing:
            skipped += 1
            continue

        slug = make_slug(title)
        lead = {
            "title": title,
            "statement": statement,
            "source_id": f"curated_{slug.replace('-', '_')}",
            "source_url": url,
            "domain_hint": domain,
            "subdomain_hint": subdomain,
            "confidence": 0.6,
            "extraction_method": "curated_list",
            "discovered_by": "bulk_generator",
            "discovered_at": TODAY,
            "status": "lead_unverified",
        }

        filename = f"bulk-{slug}.yaml"
        path = LEADS_DIR / filename
        if not path.exists():
            with open(path, "w") as f:
                yaml.dump(lead, f, default_flow_style=False, allow_unicode=True, sort_keys=False)
            existing.add(title.strip().lower())
            generated += 1

    print(f"Generated: {generated}, Skipped (duplicate): {skipped}")
    print(f"Total CURATED_PROBLEMS entries: {len(CURATED_PROBLEMS)}")

if __name__ == "__main__":
    main()
